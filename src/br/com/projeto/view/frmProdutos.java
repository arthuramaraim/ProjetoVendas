    private void dadosPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dadosPesquisarActionPerformed
        // Botão pesquisar cliente por nome e preeencher 
        
        
        
        //String txtDescricao.getText();
        String descricao = txtDescricao.getText();
        Produtos obj = new Produtos();
        ProdutosDAO dao = new ProdutosDAO();
        
        obj = dao.consultaPorNome(descricao);

        // obj = dao.consultaPorNome(nome);
        if (obj.getDescricao()!= null) {
            //Exibir os dados nos campos de textos
            txtCodigo.setText(String.valueOf(obj.getId()));
            txtDescricao.setText(obj.getDescricao());
            txtPreco.setText(String.valueOf(obj.getPreco()));
            txtQtd.setText(String.valueOf(obj.getQtd_estoque()));
            comboxFornecedor.setSelectedItem(obj.getFornecedor().getNome());
            
            
            

        } else {

            JOptionPane.showMessageDialog(null, "Produto não encontrado!");
            new Utilitarios().LimpaTela(jPanel2);
        }


    }//GEN-LAST:event_dadosPesquisarActionPerformed

