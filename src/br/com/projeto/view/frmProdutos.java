    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed
        // botão salvar

        try {
            Produtos obj = new Produtos();
            obj.setDescricao(txtDescricao.getText());
            obj.setPreco(Double.parseDouble(txtPreco.getText()));
            obj.setQtd_estoque(Integer.parseInt(txtQtd.getText()));

            //criar um objeto de Fornecedor
            Fornecedores f = new Fornecedores();
            f = (Fornecedores) comboxFornecedor.getSelectedItem();
            obj.setFornecedor(f);
            //cadastrar objeto no banco
            ProdutosDAO dao = new ProdutosDAO();
            dao.cadastrarProdutos(obj);

        } catch (Exception e) {

        }

        new Utilitarios().LimpaTela(jPanel2);
        //limparCampos();
    }//GEN-LAST:event_buttonSalvarActionPerformed

