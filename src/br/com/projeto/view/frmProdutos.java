    private void buttoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttoEditarActionPerformed
        // Botão editar
        
        Produtos obj = new Produtos();
        
        obj.setDescricao(txtDescricao.getText());
        obj.setPreco(Double.parseDouble(txtPreco.getText()));
        obj.setQtd_estoque(Integer.parseInt(txtQtd.getText()));
        obj.setFornecedor((Fornecedores) comboxFornecedor.getSelectedItem());
        
        obj.setId(Integer.parseInt(txtCodigo.getText()));
        
        //cadastrar objeto no banco
        ProdutosDAO dao = new ProdutosDAO();
        dao.alterarProduto(obj);

        new Utilitarios().LimpaTela(jPanel2);
        //limparCampos();


    }//GEN-LAST:event_buttoEditarActionPerformed

